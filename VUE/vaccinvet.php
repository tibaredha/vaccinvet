<h1>Que permet le logiciel vaccinvet ?</h1>
<ul>
<li>La vaccinologie est aujourd’hui un domaine de complexité croissante</li>
<li>Les défis et les questions auxquels sont confrontés les professionnels de la santé animale augmentent ainsi rapidement.</li>
</ul>
<ul>
<li>vaccinvet est un logiciel de gestion des vaccinations,applications sur mesure, a pour but de satisfaire :</li>
<li>d'une part la demande des professionnels de la santé humaine et animale</li>
<li>d'autre part la demande  de l administration de pouvoir facilement appliquer en pratique des recommandations de plus en plus complexes.</li>
<li>vaccinvet fonctionne en ligne.</li> 
<li>Aucune installation n'est nécessaire sur votre ordinateur.</li> 
<li>Votre logiciel est accessible sur toutes les plateformes connectées à Internet : PC, Mac, Linux</li>
<li>Enfin un logiciel véto simple, moderne et évolutif !</li>
</ul>
<ul>
<li>vaccinvet permet en particulier :</li>
<li>de générer et  d'imprimer automatiquement le certificat de vaccination ainsi que le bilan partiel de vaccination </li>
