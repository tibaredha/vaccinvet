<ul>
<li>Si vous souhaitez doter votre ordinateur de vaccinvet, vous devez disposer de la configuration requise suivante.</li>
<li>Un processeur 32 bits (x86) ou 64 bits (x64) de 1 gigahertz (GHz) ou plus rapide</li>
